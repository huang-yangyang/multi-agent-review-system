"""系统提示词管理中心 — 所有 LLM System Prompt 的统一入口。

支持：
- 版本管理：每个 prompt 有 version 字段
- 领域适配：按 domain 返回不同的 prompt
- A/B 测试就绪：外部化后可通过配置切换版本
- 热更新：修改此文件后重启即可生效，无需改动业务代码
"""

from typing import Optional

# ═══════════════════════════════════════════════════════════════
# Prompt 注册表
# ═══════════════════════════════════════════════════════════════

PROMPTS: dict = {}  # 由 _register() 自动填充


def _register(key: str, version: str, content: str, **tags):
    PROMPTS[key] = {"version": version, "content": content, "tags": tags}


def get_prompt(key: str) -> str:
    """获取 system prompt。key 不存在时返回空字符串。"""
    return PROMPTS.get(key, {}).get("content", "")


def get_prompt_version(key: str) -> str:
    return PROMPTS.get(key, {}).get("version", "unknown")


