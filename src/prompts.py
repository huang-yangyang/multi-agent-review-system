# 工具函数
# ═══════════════════════════════════════════════════════════════

def list_prompts() -> list[dict]:
    """列出所有已注册的 prompt。"""
    return [
        {"key": k, "version": v["version"], "tags": v["tags"]}
        for k, v in PROMPTS.items()
    ]


def get_prompt_for_domain(key_prefix: str, domain: str) -> str:
    """按领域获取 prompt。先精确匹配 domain，失败时用 'general'。"""
    full_key = f"{key_prefix}_{domain}"
    if full_key in PROMPTS:
        return PROMPTS[full_key]["content"]
    fallback = f"{key_prefix}_general"
    if fallback in PROMPTS:
        return PROMPTS[fallback]["content"]
    return ""
